def extract_tenant_id(event: dict) -> str:
    """Extract tenant ID from API Gateway event.

    Args:
        event: API Gateway event

    Returns:
        Tenant ID string

    Raises:
        ValueError: If tenant_id is not found
    """
    # Try authorizer context first
    tenant_id = (
        event.get("requestContext", {})
        .get("authorizer", {})
        .get("tenant_id")
    )

    # Fallback to header
    if not tenant_id:
        headers = event.get("headers", {})
        tenant_id = headers.get(TENANT_ID_HEADER) or headers.get(TENANT_ID_HEADER.lower())

    if not tenant_id:
        raise ValueError("Missing tenant_id in request")

    return tenant_id


def log_with_context(level: str, message: str, tenant_id: str, request_id: str, **kwargs):
    """Log with tenant and request context."""
    log_entry = {
        "timestamp": datetime.utcnow().isoformat(),
        "level": level,
        "message": message,
        "tenant_id": tenant_id,
        "request_id": request_id,
        **kwargs
    }
    if level == "ERROR":
        logger.error(json.dumps(log_entry))
    elif level == "WARNING":
        logger.warning(json.dumps(log_entry))
    else:
        logger.info(json.dumps(log_entry))


