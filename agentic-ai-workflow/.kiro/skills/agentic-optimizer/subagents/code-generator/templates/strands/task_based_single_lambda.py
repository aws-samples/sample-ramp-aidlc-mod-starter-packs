def lambda_handler(event: dict, context: Any) -> dict:
    """AWS Lambda handler for the Strands agent.

    Expected request body:
    {
        "task": "The task to complete"
    }

    Args:
        event: API Gateway event
        context: Lambda context

    Returns:
        API Gateway response
    """
    request_id = context.aws_request_id if context else f"local-{datetime.utcnow().isoformat()}"

    try:
        # Extract tenant ID
        tenant_id = extract_tenant_id(event)

        log_with_context(
            "INFO",
            "Request received",
            tenant_id,
            request_id
        )

        # Parse request body
        body = json.loads(event.get("body", "{}"))
        task = body.get("task")

        if not task:
            return {
                "statusCode": 400,
                "headers": {"Content-Type": "application/json"},
                "body": json.dumps({
                    "error": "Missing 'task' in request body",
                    "request_id": request_id
                })
            }

        # Create and run agent
        agent = TenantAwareAgent(tenant_id, request_id)
        result = agent.run(task)

        if result["success"]:
            return {
                "statusCode": 200,
                "headers": {"Content-Type": "application/json"},
                "body": json.dumps({
                    "result": result["result"],
                    "request_id": request_id
                })
            }
        else:
            return {
                "statusCode": 500,
                "headers": {"Content-Type": "application/json"},
                "body": json.dumps({
                    "error": result["error"],
                    "request_id": request_id
                })
            }

    except ValueError as e:
        return {
            "statusCode": 403,
            "headers": {"Content-Type": "application/json"},
            "body": json.dumps({
                "error": str(e),
                "request_id": request_id
            })
        }

    except json.JSONDecodeError:
        return {
            "statusCode": 400,
            "headers": {"Content-Type": "application/json"},
            "body": json.dumps({
                "error": "Invalid JSON in request body",
                "request_id": request_id
            })
        }

    except Exception as e:
        logger.exception("Unexpected error")
        return {
            "statusCode": 500,
            "headers": {"Content-Type": "application/json"},
            "body": json.dumps({
                "error": "Internal server error",
                "request_id": request_id
            })
        }


