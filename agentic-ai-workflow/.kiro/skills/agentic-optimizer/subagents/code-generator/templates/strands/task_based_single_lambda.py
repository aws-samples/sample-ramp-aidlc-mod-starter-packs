@tool
def call_external_api(endpoint: str, method: str = "GET", body: Optional[dict] = None) -> str:
    """Call an external API endpoint.

    Args:
        endpoint: API endpoint URL (must be HTTPS and resolve to a public IP)
        method: HTTP method (GET, POST, etc.)
        body: Request body for POST/PUT requests

    Returns:
        API response as string
    """
    import urllib.request
    import urllib.error
    import socket
    import ipaddress
    from urllib.parse import urlparse

    # SSRF protection: enforce HTTPS and block requests to private, loopback,
    # link-local (incl. the 169.254.169.254 instance metadata endpoint),
    # and other non-public address ranges.
    parsed = urlparse(endpoint)
    if parsed.scheme != "https":
        return "API call rejected: only HTTPS endpoints are allowed"
    if not parsed.hostname:
        return "API call rejected: invalid endpoint URL"

    try:
        resolved = socket.getaddrinfo(parsed.hostname, parsed.port or 443, proto=socket.IPPROTO_TCP)
    except socket.gaierror as e:
        return f"API call rejected: DNS resolution failed ({e})"

    for family, _, _, _, sockaddr in resolved:
        ip = ipaddress.ip_address(sockaddr[0])
        if (
            ip.is_private
            or ip.is_loopback
            or ip.is_link_local
            or ip.is_reserved
            or ip.is_multicast
            or ip.is_unspecified
        ):
            return "API call rejected: endpoint resolves to a non-public address"

    try:
        req = urllib.request.Request(endpoint, method=method)
        if body:
            req.data = json.dumps(body).encode()
            req.add_header("Content-Type", "application/json")

        with urllib.request.urlopen(req, timeout=30) as response:  # nosec B310 - scheme validated above
            return response.read().decode()

    except urllib.error.URLError as e:
        return f"API call failed: {str(e)}"


