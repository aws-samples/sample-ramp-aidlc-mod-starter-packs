class TenantAwareAgent:
    """Wrapper for Strands Agent with tenant context."""

    def __init__(self, tenant_id: str, request_id: str):
        """Initialize agent with tenant context.

        Args:
            tenant_id: Tenant identifier
            request_id: Request identifier for tracing
        """
        self.tenant_id = tenant_id
        self.request_id = request_id

        # Create Bedrock model
        self.model = BedrockModel(
            model_id=MODEL_ID,
            region_name=AWS_REGION
        )

        # Create agent with tools
        self.agent = Agent(
            model=self.model,
            system_prompt=self._build_system_prompt(),
            tools=[
                search_database,
                call_external_api,
                store_result,
                calculate
            ]
        )

    def _build_system_prompt(self) -> str:
        """Build system prompt with tenant context."""
        return f"""You are a helpful AI assistant operating in a multi-tenant environment.

Current Context:
- Tenant ID: {self.tenant_id}
- Request ID: {self.request_id}

Guidelines:
1. Always consider the tenant context when accessing data
2. Use tools appropriately to complete tasks
3. Be concise but thorough in your responses
4. If you're unsure about something, ask for clarification
5. Prioritize security and data isolation

Your capabilities:
- Search the tenant's database for information
- Call external APIs when needed
- Store results for future reference
- Perform calculations

Complete tasks efficiently while maintaining data isolation between tenants."""

    @xray_recorder.capture("agent_run")
    def run(self, task: str) -> dict:
        """Execute a task with the agent.

        Args:
            task: Task description

        Returns:
            Dictionary with result and metadata
        """
        log_with_context(
            "INFO",
            "Agent execution started",
            self.tenant_id,
            self.request_id,
            task_length=len(task)
        )

        try:
            # Execute agent
            result = self.agent(task)

            log_with_context(
                "INFO",
                "Agent execution completed",
                self.tenant_id,
                self.request_id,
                result_length=len(str(result))
            )

            return {
                "success": True,
                "result": str(result),
                "tenant_id": self.tenant_id,
                "request_id": self.request_id
            }

        except Exception as e:
            log_with_context(
                "ERROR",
                "Agent execution failed",
                self.tenant_id,
                self.request_id,
                error=str(e)
            )

            return {
                "success": False,
                "error": str(e),
                "tenant_id": self.tenant_id,
                "request_id": self.request_id
            }


