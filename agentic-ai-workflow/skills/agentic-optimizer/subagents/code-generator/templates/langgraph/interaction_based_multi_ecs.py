def supervisor_agent(state: ConversationState) -> ConversationState:
    """Supervisor agent that routes conversations to appropriate specialists.

    Analyzes the user's message and determines which specialist agent
    should handle the request.
    """
    llm = create_bedrock_llm(state["tenant_id"])

    system_prompt = """You are a supervisor agent that routes user requests to specialists.

Available specialists:
- RESEARCHER: For questions requiring information lookup or analysis
- ASSISTANT: For general questions and task completion
- SPECIALIST: For domain-specific technical questions

Analyze the user's message and respond with ONLY the agent name to route to.
If the conversation seems complete, respond with FINISH."""

    last_message = state["messages"][-1] if state["messages"] else None
    if not last_message:
        state["current_agent"] = "assistant"
        return state

    response = llm.invoke([
        SystemMessage(content=system_prompt),
        HumanMessage(content=f"Route this message: {last_message.content}")
    ])

    routing = response.content.strip().upper()

    if "RESEARCHER" in routing:
        state["current_agent"] = "researcher"
    elif "SPECIALIST" in routing:
        state["current_agent"] = "specialist"
    elif "FINISH" in routing:
        state["current_agent"] = "finish"
    else:
        state["current_agent"] = "assistant"

    return state


def researcher_agent(state: ConversationState) -> ConversationState:
    """Researcher agent for information gathering and analysis."""
    llm = create_bedrock_llm(state["tenant_id"])

    system_prompt = """You are a research specialist. Your role is to:
1. Analyze questions thoroughly
2. Provide well-researched, accurate information
3. Cite sources when possible
4. Ask clarifying questions if needed

Be thorough but concise in your responses."""

    response = llm.invoke([
        SystemMessage(content=system_prompt),
        *state["messages"]
    ])

    state["messages"] = [response]
    state["agent_outputs"]["researcher"] = response.content
    return state


