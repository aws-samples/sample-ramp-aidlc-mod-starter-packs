def route_to_agent(state: ConversationState) -> str:
    """Route to the appropriate agent based on supervisor decision."""
    return state["current_agent"]


def create_conversation_graph() -> StateGraph:
    """Create the multi-agent conversation graph."""
    workflow = StateGraph(ConversationState)

    # Add nodes
    workflow.add_node("supervisor", supervisor_agent)
    workflow.add_node("researcher", researcher_agent)
    workflow.add_node("assistant", assistant_agent)
    workflow.add_node("specialist", specialist_agent)

    # Define edges
    workflow.set_entry_point("supervisor")

    workflow.add_conditional_edges(
        "supervisor",
        route_to_agent,
        {
            "researcher": "researcher",
            "assistant": "assistant",
            "specialist": "specialist",
            "finish": END
        }
    )

    # All agents return to supervisor for potential follow-up
    workflow.add_edge("researcher", END)
    workflow.add_edge("assistant", END)
    workflow.add_edge("specialist", END)

    # Use memory saver for conversation persistence
    memory = MemorySaver()
    return workflow.compile(checkpointer=memory)


# Initialize graph at module load
conversation_graph = create_conversation_graph()


