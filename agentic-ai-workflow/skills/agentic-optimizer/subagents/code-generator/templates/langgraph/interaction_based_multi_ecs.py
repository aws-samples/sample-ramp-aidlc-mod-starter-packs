@app.route("/chat", methods=["POST"])
def chat():
    """Handle chat messages in a conversation.

    Expected request body:
    {
        "message": "User's message",
        "session_id": "optional-session-id"
    }
    """
    try:
        tenant_id = extract_tenant_id()
        request_id = request.headers.get("x-request-id", datetime.utcnow().isoformat())

        log = logger.bind(
            tenant_id=tenant_id,
            request_id=request_id
        )

        body = request.get_json()
        message = body.get("message")
        session_id = body.get("session_id", f"{tenant_id}-{datetime.utcnow().timestamp()}")

        if not message:
            return jsonify({"error": "Missing 'message' in request body"}), 400

        log.info("chat_request", session_id=session_id, message_length=len(message))

        # Get or create conversation state
        conversation_key = f"{tenant_id}:{session_id}"
        existing_messages = conversation_store.get(conversation_key, [])

        # Create initial state
        initial_state: ConversationState = {
            "messages": existing_messages + [HumanMessage(content=message)],
            "tenant_id": tenant_id,
            "session_id": session_id,
            "current_agent": "",
            "agent_outputs": {},
            "metadata": {
                "request_id": request_id,
                "timestamp": datetime.utcnow().isoformat()
            }
        }

        # Run the conversation graph
        config = {"configurable": {"thread_id": conversation_key}}
        final_state = conversation_graph.invoke(initial_state, config)

        # Store updated conversation
        conversation_store[conversation_key] = final_state["messages"]

        # Get the last AI response
        ai_messages = [m for m in final_state["messages"] if isinstance(m, AIMessage)]
        response_content = ai_messages[-1].content if ai_messages else "I couldn't process that request."

        log.info("chat_response", session_id=session_id, agent=final_state.get("current_agent"))

        return jsonify({
            "response": response_content,
            "session_id": session_id,
            "agent_used": final_state.get("current_agent"),
            "request_id": request_id
        })

    except ValueError as e:
        return jsonify({"error": str(e)}), 403
    except Exception as e:
        logger.error("chat_error", error=str(e))
        return jsonify({"error": "Internal server error"}), 500


@app.route("/sessions/<session_id>", methods=["GET"])
def get_session():
    """Get conversation history for a session."""
    try:
        tenant_id = extract_tenant_id()
        session_id = request.view_args.get("session_id")

        conversation_key = f"{tenant_id}:{session_id}"
        messages = conversation_store.get(conversation_key, [])

        history = [
            {
                "role": "user" if isinstance(m, HumanMessage) else "assistant",
                "content": m.content
            }
            for m in messages
        ]

        return jsonify({
            "session_id": session_id,
            "message_count": len(messages),
            "history": history
        })

    except ValueError as e:
        return jsonify({"error": str(e)}), 403


@app.route("/sessions/<session_id>", methods=["DELETE"])
def delete_session():
    """Clear a conversation session."""
    try:
        tenant_id = extract_tenant_id()
        session_id = request.view_args.get("session_id")

        conversation_key = f"{tenant_id}:{session_id}"
        if conversation_key in conversation_store:
            del conversation_store[conversation_key]

        return jsonify({"status": "deleted", "session_id": session_id})

    except ValueError as e:
        return jsonify({"error": str(e)}), 403


