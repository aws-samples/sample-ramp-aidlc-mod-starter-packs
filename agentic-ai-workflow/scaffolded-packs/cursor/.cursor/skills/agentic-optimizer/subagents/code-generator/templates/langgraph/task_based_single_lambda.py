def should_continue_execution(state: AgentState) -> str:
    """Determine if we should continue executing steps or move to synthesis.

    Args:
        state: Current agent state

    Returns:
        Next node name: "execute_step", "synthesize", or "error"
    """
    if state.get("error"):
        return "end"

    if state["current_step"] < len(state["steps"]):
        return "execute_step"

    return "synthesize"


def create_agent_graph() -> StateGraph:
    """Create the LangGraph workflow for task execution.

    Returns:
        Compiled StateGraph
    """
    workflow = StateGraph(AgentState)

    # Add nodes
    workflow.add_node("analyze", analyze_task)
    workflow.add_node("execute_step", execute_step)
    workflow.add_node("synthesize", synthesize_result)

    # Define edges
    workflow.set_entry_point("analyze")

    workflow.add_conditional_edges(
        "analyze",
        lambda s: "end" if s.get("error") else "execute_step",
        {
            "execute_step": "execute_step",
            "end": END
        }
    )

    workflow.add_conditional_edges(
        "execute_step",
        should_continue_execution,
        {
            "execute_step": "execute_step",
            "synthesize": "synthesize",
            "end": END
        }
    )

    workflow.add_edge("synthesize", END)

    return workflow.compile()


