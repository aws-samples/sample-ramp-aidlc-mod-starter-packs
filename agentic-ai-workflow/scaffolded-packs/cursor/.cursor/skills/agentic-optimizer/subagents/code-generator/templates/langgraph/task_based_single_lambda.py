@xray_recorder.capture("analyze_task")
def analyze_task(state: AgentState) -> AgentState:
    """Analyze the task and break it into execution steps.

    This node uses the LLM to understand the task and create
    a plan of action.

    Args:
        state: Current agent state

    Returns:
        Updated state with steps populated
    """
    log_with_context(
        "INFO",
        "Analyzing task",
        state["tenant_id"],
        state["request_id"],
        task_length=len(state["task_input"])
    )

    try:
        llm = create_bedrock_client(state["tenant_id"])

        system_prompt = """You are a task analysis expert. Your job is to:
1. Understand the user's task
2. Break it down into 3-5 clear, actionable steps
3. Return ONLY the steps as a numbered list

Be specific and practical. Each step should be independently executable."""

        user_prompt = f"""Analyze this task and break it into execution steps:

Task: {state["task_input"]}

Provide 3-5 concrete steps to complete this task. Return only the numbered list."""

        response = llm.invoke([
            SystemMessage(content=system_prompt),
            HumanMessage(content=user_prompt)
        ])

        # Parse steps from response
        steps = []
        for line in response.content.strip().split("\n"):
            line = line.strip()
            if line and (line[0].isdigit() or line.startswith("-")):
                # Remove numbering/bullets
                step = line.lstrip("0123456789.-) ").strip()
                if step:
                    steps.append(step)

        if not steps:
            steps = ["Process the task as a single operation"]

        state["steps"] = steps
        state["current_step"] = 0

        log_with_context(
            "INFO",
            "Task analyzed",
            state["tenant_id"],
            state["request_id"],
            step_count=len(steps)
        )

    except Exception as e:
        state["error"] = f"Task analysis failed: {str(e)}"
        log_with_context(
            "ERROR",
            "Task analysis failed",
            state["tenant_id"],
            state["request_id"],
            error=str(e)
        )

    return state


@xray_recorder.capture("execute_step")
def execute_step(state: AgentState) -> AgentState:
    """Execute the current step in the task plan.

    Args:
        state: Current agent state

    Returns:
        Updated state with step result
    """
    if state.get("error"):
        return state

    current_idx = state["current_step"]
    if current_idx >= len(state["steps"]):
        return state

    current_step = state["steps"][current_idx]

    log_with_context(
        "INFO",
        f"Executing step {current_idx + 1}",
        state["tenant_id"],
        state["request_id"],
        step=current_step
    )

    try:
        llm = create_bedrock_client(state["tenant_id"])

        # Build context from previous steps
        context = ""
        if state["intermediate_results"]:
            context = "\n\nPrevious step results:\n"
            for i, result in enumerate(state["intermediate_results"]):
                context += f"Step {i + 1}: {result[:200]}...\n"

        prompt = f"""You are executing a step in a larger task.

Original Task: {state["task_input"]}

Current Step ({current_idx + 1}/{len(state["steps"])}): {current_step}
{context}

Execute this step and provide the result. Be concise but complete."""

        response = llm.invoke([HumanMessage(content=prompt)])

        state["intermediate_results"].append(response.content)
        state["current_step"] = current_idx + 1

        log_with_context(
            "INFO",
            f"Step {current_idx + 1} completed",
            state["tenant_id"],
            state["request_id"],
            result_length=len(response.content)
        )

    except Exception as e:
        state["error"] = f"Step {current_idx + 1} failed: {str(e)}"
        log_with_context(
            "ERROR",
            f"Step {current_idx + 1} failed",
            state["tenant_id"],
            state["request_id"],
            error=str(e)
        )

    return state


@xray_recorder.capture("synthesize_result")
def synthesize_result(state: AgentState) -> AgentState:
    """Synthesize final result from all step outputs.

    Args:
        state: Current agent state

    Returns:
        Updated state with final result
    """
    if state.get("error"):
        return state

    log_with_context(
        "INFO",
        "Synthesizing final result",
        state["tenant_id"],
        state["request_id"]
    )

    try:
        llm = create_bedrock_client(state["tenant_id"])

        # Compile all intermediate results
        results_summary = "\n\n".join([
            f"Step {i + 1} ({state['steps'][i]}):\n{result}"
            for i, result in enumerate(state["intermediate_results"])
        ])

        prompt = f"""You have completed a multi-step task. Now synthesize the final result.

Original Task: {state["task_input"]}

Step Results:
{results_summary}

Provide a clear, comprehensive final answer that addresses the original task.
Synthesize the step results into a coherent response."""

        response = llm.invoke([HumanMessage(content=prompt)])
        state["final_result"] = response.content

        log_with_context(
            "INFO",
            "Result synthesized",
            state["tenant_id"],
            state["request_id"],
            result_length=len(state["final_result"])
        )

    except Exception as e:
        state["error"] = f"Result synthesis failed: {str(e)}"
        # Fallback: concatenate intermediate results
        state["final_result"] = "\n\n".join(state["intermediate_results"])
        log_with_context(
            "WARNING",
            "Synthesis failed, using concatenated results",
            state["tenant_id"],
            state["request_id"],
            error=str(e)
        )

    return state


