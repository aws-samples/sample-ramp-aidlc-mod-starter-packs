def lambda_handler(event: dict, context) -> dict:
    """AWS Lambda handler for the task-based agent.

    Expected request body:
    {
        "task": "The task to complete"
    }

    Args:
        event: API Gateway event
        context: Lambda context

    Returns:
        API Gateway response with result or error
    """
    request_id = context.aws_request_id if context else "local-" + datetime.utcnow().isoformat()

    try:
        # Extract tenant ID
        tenant_id = extract_tenant_id(event)

        log_with_context(
            "INFO",
            "Request received",
            tenant_id,
            request_id,
            path=event.get("path"),
            method=event.get("httpMethod")
        )

        # Parse request body
        body = json.loads(event.get("body", "{}"))
        task_input = body.get("task")

        if not task_input:
            return {
                "statusCode": 400,
                "headers": {"Content-Type": "application/json"},
                "body": json.dumps({
                    "error": "Missing 'task' in request body",
                    "request_id": request_id
                })
            }

        # Initialize state
        initial_state: AgentState = {
            "task_input": task_input,
            "tenant_id": tenant_id,
            "request_id": request_id,
            "steps": [],
            "current_step": 0,
            "intermediate_results": [],
            "final_result": "",
            "error": None,
            "metadata": {
                "start_time": datetime.utcnow().isoformat(),
                "model_id": MODEL_ID
            }
        }

        # Execute agent graph
        final_state = agent_graph.invoke(initial_state)

        # Check for errors
        if final_state.get("error"):
            log_with_context(
                "ERROR",
                "Agent execution failed",
                tenant_id,
                request_id,
                error=final_state["error"]
            )
            return {
                "statusCode": 500,
                "headers": {"Content-Type": "application/json"},
                "body": json.dumps({
                    "error": final_state["error"],
                    "request_id": request_id
                })
            }

        # Success response
        log_with_context(
            "INFO",
            "Request completed successfully",
            tenant_id,
            request_id,
            steps_executed=len(final_state["steps"])
        )

        return {
            "statusCode": 200,
            "headers": {"Content-Type": "application/json"},
            "body": json.dumps({
                "result": final_state["final_result"],
                "steps_executed": final_state["steps"],
                "request_id": request_id
            })
        }

    except ValueError as e:
        # Tenant ID missing or invalid
        return {
            "statusCode": 403,
            "headers": {"Content-Type": "application/json"},
            "body": json.dumps({
                "error": str(e),
                "request_id": request_id
            })
        }

    except json.JSONDecodeError:
        return {
            "statusCode": 400,
            "headers": {"Content-Type": "application/json"},
            "body": json.dumps({
                "error": "Invalid JSON in request body",
                "request_id": request_id
            })
        }

    except Exception as e:
        logger.exception("Unexpected error")
        return {
            "statusCode": 500,
            "headers": {"Content-Type": "application/json"},
            "body": json.dumps({
                "error": "Internal server error",
                "request_id": request_id
            })
        }


